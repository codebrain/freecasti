def _format_encoding_source(
    row: dict[str, Any],
    *,
    max_preset_links: int = MAX_INLINE_PRESET_LINKS,
) -> str:
    """Render Source cell; list every witness, comma-separated.

    When many presets witness one step, inline only the first *max_preset_links*
    and append a ``+N more`` suffix. Full lists appear in
    :func:`_preset_witnesses_appendix`.
    """
    sources = list(row.get("sources") or [])
    if not sources and row.get("source"):
        sources = [str(row["source"])]
    parts: list[str] = []
    for source in sources:
        if source == "preset":
            refs = row.get("preset_refs") or []
            if not refs:
                parts.append("preset")
                continue
            for i, (bank, preset) in enumerate(refs[:max_preset_links], start=1):
                parts.append(_preset_link(bank, preset, i))
            extra = len(refs) - max_preset_links
            if extra > 0:
                parts.append(f"+{extra} more")
        else:
            parts.append(str(source))
    return ", ".join(parts)


def _preset_witnesses_appendix(
    densified: list[dict[str, Any]],
    *,
    max_inline: int = MAX_INLINE_PRESET_LINKS,
) -> str:
    """Full preset witness lists for encoding rows with collapsed inline links."""
    sections: list[str] = []
    for row in densified:
        refs = row.get("preset_refs") or []
        if len(refs) <= max_inline:
            continue
        enc = int(row["encoded"])
        by_bank: dict[str, list[str]] = {}
        for bank, preset in refs:
            by_bank.setdefault(bank, []).append(
                preset_md_link(bank, preset, prefix="../", page=True)
            )
        bank_parts = [
            f"**{bank}:** {', '.join(links)}"
            for bank, links in sorted(by_bank.items())
        ]
        sections.append(f"- **{enc}:** " + "; ".join(bank_parts))
    if not sections:
        return ""
    lines = [
        "### Preset witnesses",
        "",
        "Factory presets that witness encoded steps with more than "
        f"{max_inline} matches (collapsed in the table above):",
        "",
        *sections,
    ]
    return "\n".join(lines)


def _encoding_map_table(
    result: dict[str, Any],
    best: dict[str, Any],
    densified: list[dict[str, Any]],
) -> str:
    """Full encoded→label map with Source (dump / provided / preset)."""
    from ..preset_inferred import pack_encoded_bytes

    primary = list(best["offsets"])
    headers = ["Encoded"]
    for off in primary:
        headers.append(f"Offset {off}")
    headers.extend(["Label", "Source"])

    lines = [
        ENCODING_MAP_INTRO_SHORT,
        "",
        "| " + " | ".join(headers) + " |",
        "| " + " | ".join("---" for _ in headers) + " |",
    ]

    for row in densified:
        enc = int(row["encoded"])
        nibbles = pack_encoded_bytes(enc, best["encoding"], len(primary))
        label = _format_encoding_label(result, row)
        cells = [str(enc)]
        cells.extend(f"`{b}`" for b in nibbles)
        # Pad if encoding produced fewer bytes than primary offsets.
        while len(cells) < 1 + len(primary):
            cells.append("-")
        cells.append(label)
        cells.append(_format_encoding_source(row))
        lines.append("| " + " | ".join(cells) + " |")
    return "\n".join(lines)


def _dump_table(
    result: dict[str, Any],
    best: dict[str, Any],
    *,
    include_source: bool = False,
) -> str:
    primary = list(best["offsets"])
    secondary = result.get("classification", {}).get("secondary_offsets") or []
    checksum = result.get("classification", {}).get("checksum_candidate_offsets") or []
    series_unit = result.get("unit")

    headers = ["Label"]
    for off in primary:
        headers.append(f"Offset {off}")
    if len(primary) >= 2:
        headers.append(f"`{best['encoding']}`")
    elif primary:
        headers.append("Encoded")
    for off in secondary:
        headers.append(f"Offset {off}")
    if checksum:
        headers.append(
            f"Checksum {checksum[0]}-{checksum[-1]}"
            if len(checksum) > 1
            else f"Checksum {checksum[0]}"
        )
    if include_source:
        headers.append("Source")

    rows = ["| " + " | ".join(headers) + " |"]
    rows.append("| " + " | ".join("---" for _ in headers) + " |")

    for dump in result.get("dumps") or []:
        label = _dump_label_cell(dump, series_unit=series_unit)
        cells = [label]
        changing = dump.get("changing_bytes") or {}
        for off in primary:
            cells.append(f"`{changing.get(str(off), '??')}`")
        decoded = dump.get("decoded_parameter") or {}
        if "encoded_value" in decoded:
            cells.append(str(decoded["encoded_value"]))
        elif primary:
            cells.append("-")
        for off in secondary:
            cells.append(f"`{changing.get(str(off), '??')}`")
        if checksum:
            cs = " ".join(f"`{changing.get(str(off), '??')}`" for off in checksum)
            cells.append(cs)
        if include_source:
            cells.append("dump")
        rows.append("| " + " | ".join(cells) + " |")

    return "\n".join(rows)


def _dump_label_cell(dump: dict[str, Any], series_unit: str | None = None) -> str:
    from ..labels import format_value_with_unit

    label = dump.get("label") or {}
    kind = label.get("kind")
    unit = label.get("unit") or series_unit
    decoded = dump.get("decoded_parameter") or {}

    if label.get("pair"):
        from ..labels import format_balance_pair

        a, b = label["pair"]
        return format_balance_pair(a, b)
    if kind == "endpoint":
        name = label.get("stem") or label.get("endpoint") or label.get("value")
        inferred = decoded.get("inferred_label")
        if inferred is not None:
            return f"{name} ({format_value_with_unit(inferred, unit)})"
        return str(name)
    if kind == "number":
        return format_value_with_unit(label.get("value"), unit)
    if kind == "bool":
        return "on" if label.get("value") else "off"
    return str(label.get("stem") or dump.get("file") or "?")


def _fmt_offsets(offsets: list[int]) -> str:
    if not offsets:
        return "-"
    if len(offsets) == 1:
        return str(offsets[0])
    runs: list[str] = []
    start = prev = offsets[0]
    for off in offsets[1:]:
        if off == prev + 1:
            prev = off
            continue
        runs.append(str(start) if start == prev else f"{start}-{prev}")
        start = prev = off
    runs.append(str(start) if start == prev else f"{start}-{prev}")
    return ", ".join(runs)


def _short_notes(best: dict[str, Any] | None) -> str:
    if not best:
        return "-"
    notes = best.get("notes") or ""
    primary = notes.split(";", 1)[0].strip()
    if primary.startswith("label = encoded * 1"):
        return "identity"
    if primary.startswith("label = "):
        return primary.removeprefix("label = ").strip()
    if not primary:
        return "-"
    return primary


