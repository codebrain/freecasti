def _render_parameter_page(
    result: dict[str, Any],
    *,
    all_results: list[dict[str, Any]],
    today: str,
    sysex_root: Path | None = None,
) -> str:
    name = result["parameter"]
    display = _parameter_display_name(name, kind="prog")
    lines: list[str] = [
        _page_nav(depth=1),
        f"# {display}",
        "",
        f"_Generated {today}. Source folder: `sysex/prog/parameters/{name}/`._",
        "",
    ]
    lines.extend(_parameter_synopsis(result, kind="prog"))
    lines.extend(
        _provided_coverage_note(name, sysex_root=sysex_root, kind="prog")
    )
    lines.extend(
        _parameter_description_section(
            result.get("official"), parameter=name
        )
    )
    lines.extend(
        [
            _parameter_body(result, sysex_root=sysex_root),
            "",
            "## Other parameters",
            "",
        ]
    )
    for other in all_results:
        other_name = other["parameter"]
        slug = parameter_slug(other_name)
        other_display = _parameter_display_name(other_name, kind="prog")
        if other_name == name:
            lines.append(f"- **{other_display}** (this page)")
        else:
            lines.append(f"- [{other_display}]({slug}.md)")
    lines.append("")
    return "\n".join(lines)


def _parameter_body(
    result: dict[str, Any],
    *,
    sysex_root: Path | None = None,
) -> str:
    """Parameter dump table + interpretation (no H1/H2 title)."""
    best = result.get("best_encoding")
    hyp = result.get("hypothesis") or {}
    value_range = result.get("value_range")
    secondary = result.get("classification", {}).get("secondary_offsets") or []
    checksum = result.get("classification", {}).get("checksum_candidate_offsets") or []

    lines: list[str] = []

    if best:
        densified = _full_encoding_rows(result, best, sysex_root=sysex_root)
        if densified:
            lines.append("## Encoding map")
            lines.append("")
            lines.append(_encoding_map_table(result, best, densified))
            appendix = _preset_witnesses_appendix(densified)
            if appendix:
                lines.append("")
                lines.append(appendix)
            lines.append("")
            lines.append("### Captured dumps")
            lines.append("")
            lines.append(
                "Sparse series used as anchors (plus secondary/checksum bytes "
                "that moved with this capture stream):"
            )
            lines.append("")
            lines.append(_dump_table(result, best, include_source=True))
        else:
            lines.append(_dump_table(result, best, include_source=True))
        lines.append("")
        lines.append("## Interpretation")
        lines.append("")
        lines.append(
            f"- **Primary field:** offsets **{_fmt_offsets(best['offsets'])}**, "
            f"encoding `{best['encoding']}`"
            + (f" ({_short_notes(best)})" if _short_notes(best) != "-" else "")
            + "."
        )
        conf = hyp.get("confidence", "unknown")
        score = best.get("score")
        if score is not None:
            lines.append(
                f"- **Confidence:** {conf} "
                f"({best.get('matched', '?')}/{best.get('total', '?')} dumps matched, "
                f"{score:.0%})."
            )
        else:
            lines.append(f"- **Confidence:** {conf}.")
        official = result.get("official") or {}
        if official.get("matched"):
            exp = official.get("expected") or {}
            lines.append(
                f"- **Catalog hint (Bricasti):** {official.get('name')} - "
                f"printed {exp.get('manual_min')} ... {exp.get('manual_max')}"
                + (f" {exp['unit']}" if exp.get("unit") else "")
                + f" [{official.get('agreement', '?')}]"
                + " - hint only; dumps win."
            )
            if official.get("notes"):
                lines.append(f"- **Catalog notes:** {official['notes']}")
            for note in official.get("notes_from_check") or []:
                lines.append(f"- **Catalog note:** {note}")
            for warn in official.get("warnings") or []:
                lines.append(f"- **Catalog warning:** {warn}")
        elif official.get("warning"):
            lines.append(f"- **Catalog:** {official['warning']}")
        if value_range and value_range.get("summary"):
            lines.append(f"- **Range:** {value_range['summary']}.")
            if value_range.get("balance"):
                lines.append(
                    f"- **Display:** {value_range['balance']['display']}."
                )
            for which, title in (("off", "OFF"), ("low", "LOW"), ("high", "HIGH/FULL")):
                ep = value_range.get(which)
                if not ep or "encoded_value" not in ep:
                    continue
                if "inferred_label_display" in ep:
                    extra = f" (label {ep['inferred_label_display']})"
                elif "inferred_label" in ep:
                    extra = f" (label {ep['inferred_label']})"
                elif ep.get("note"):
                    extra = f" - {ep['note']}"
                else:
                    extra = ""
                lines.append(
                    f"- **{title} endpoint:** `{ep['file']}` -> encoded "
                    f"{ep['encoded_value']}{extra}."
                )
        sampling = result.get("sampling") or {}
        coverage = sampling.get("coverage") or {}
        if coverage:
            parts = []
            if coverage.get("has_extreme_low") and coverage.get("has_extreme_high"):
                parts.append("extremes")
            if coverage.get("has_adjacent_low") and coverage.get("has_adjacent_high"):
                parts.append("adjacents")
            if coverage.get("mid_count"):
                parts.append(
                    f"{coverage['mid_count']} sample mid(s) "
                    "(not every step)"
                )
            if parts:
                lines.append(f"- **Sampling:** {', '.join(parts)}.")
        edge = (value_range or {}).get("edge_steps") or sampling.get("edge_steps") or {}
        if edge.get("note"):
            lines.append(f"- **Edge slopes:** {edge['note']}")
        how = (hyp.get("how_to_set") or {}).get("encode_steps") or []
        if how:
            lines.append("- **How to set:**")
            for i, step in enumerate(how, start=1):
                lines.append(f"  {i}. {step}")
        if secondary:
            lines.append(
                f"- **Secondary offsets:** {_fmt_offsets(secondary)} "
                "(likely edit/UI state, not the parameter word)."
            )
        if checksum:
            cs_range = _checksum_range_desc(result)
            lines.append(
                f"- **Checksum nibbles:** {_fmt_offsets(checksum)} "
                f"(CRC-16/ARC over {cs_range}, packed high-nibble-first)."
            )
    else:
        lines.append("_No encoding hypothesis yet - need more labeled dumps._")

    lines.append("")
    return "\n".join(lines)


