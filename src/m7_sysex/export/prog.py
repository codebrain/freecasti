def export_sysex_format(
    results: list[dict[str, Any]],
    output_path: Path,
    byte_map: dict[str, Any] | None = None,
    cross: dict[str, Any] | None = None,
    names: dict[str, Any] | None = None,
    sheet_compare: dict[str, Any] | None = None,
) -> Path:
    """Write ``specification/prog/`` with overview + one page per parameter.

    Returns the prog overview README path.
    """
    if not results:
        raise ValueError("no analysis results to export")

    from ..cross import render_cross_markdown
    from ..provided import clear_provided_cache
    from ..preset_sheet import write_sheet_markdown

    # Always re-read docs/reference/provided_labels.json for this export.
    clear_provided_cache()

    if names is not None:
        from ..decode_preset import enrich_names_with_parameters

        enrich_names_with_parameters(names, results)

    output_root = resolve_export_dir(output_path)
    output_dir = prog_export_dir(output_root)
    output_dir.mkdir(parents=True, exist_ok=True)
    params_dir = output_dir / "parameters"
    params_dir.mkdir(parents=True, exist_ok=True)

    frame = results[0]["frame"]
    message_length = results[0]["message_length"]
    mfr = frame["manufacturer_id"]
    header = frame["header"]
    sorted_results = sorted(results, key=lambda r: r["parameter"])
    today = date.today().isoformat()

    overview = _render_overview_page(
        sorted_results,
        byte_map=byte_map,
        names=names,
        sheet_compare=sheet_compare,
        mfr=mfr,
        header=header,
        message_length=message_length,
        today=today,
    )
    overview_path = output_dir / "README.md"
    overview_path.write_text(overview, encoding="utf-8")

    folder = Path(results[0]["folder"])
    # ``folder`` is ``sysex/prog/parameters/<name>/`` in the type-first layout.
    sysex_root = folder.parent.parent.parent
    for result in sorted_results:
        page = _render_parameter_page(
            result,
            all_results=sorted_results,
            today=today,
            sysex_root=sysex_root,
        )
        parameter_page_path(output_dir, result["parameter"]).write_text(
            page, encoding="utf-8"
        )

    from .parameters_index import build_prog_parameters_readme

    sheet_decoders = (names or {}).get("parameter_decoders")
    (params_dir / "README.md").write_text(
        build_prog_parameters_readme(
            sorted_results,
            today=today,
            sheet_decoders=sheet_decoders,
        ),
        encoding="utf-8",
    )

    if names:
        series_names = {r["parameter"] for r in sorted_results}
        for dec in names.get("parameter_decoders") or []:
            if dec.get("source") != "preset_sheet":
                continue
            # Never clobber a full capture-series parameter page.
            if dec.get("parameter") in series_names:
                continue
            page = _render_sheet_only_parameter_page(dec, today=today)
            parameter_page_path(output_dir, dec["parameter"]).write_text(
                page, encoding="utf-8"
            )
        _export_preset_docs(
            output_dir, names, today=today, sheet_compare=sheet_compare
        )
        (output_dir / "program-identity.md").write_text(
            _render_names_page(names, today=today, sheet_compare=sheet_compare),
            encoding="utf-8",
        )

    if sheet_compare:
        write_sheet_markdown(
            sheet_compare,
            output_dir / "preset-sheet.md",
            today=today,
            nav=_page_nav(depth=0, current="Preset sheet"),
        )

    if byte_map:
        from ..byte_map import (
            render_byte_map_markdown,
            render_byte_map_overview_markdown,
        )
        from ..kaitai_spec import write_program_dump_spec

        overview_body = _promote_h1(render_byte_map_overview_markdown(byte_map))
        (output_dir / "byte-map-overview.md").write_text(
            _page_nav(depth=0, current="Byte map")
            + overview_body
            + f"\n\n_Last exported: {today}_\n",
            encoding="utf-8",
        )
        body = _promote_h1(render_byte_map_markdown(byte_map))
        (output_dir / "byte-map.md").write_text(
            _page_nav(depth=0) + body + f"\n\n_Last exported: {today}_\n",
            encoding="utf-8",
        )
        write_program_dump_spec(
            output_dir, byte_map, sorted_results, names=names
        )

    if cross:
        body = _promote_h1(render_cross_markdown(cross))
        (output_dir / "cross.md").write_text(
            _page_nav(depth=0) + body + f"\n\n_Last exported: {today}_\n",
            encoding="utf-8",
        )

    return overview_path



def _promote_h1(markdown: str) -> str:
    """Turn a leading ## heading into # for a standalone page."""
    if markdown.startswith("## "):
        return "#" + markdown[2:]
    return markdown



def _render_overview_page(
    results: list[dict[str, Any]],
    *,
    byte_map: dict[str, Any] | None,
    names: dict[str, Any] | None = None,
    sheet_compare: dict[str, Any] | None = None,
    mfr: str,
    header: str,
    message_length: int,
    today: str,
) -> str:
    parts: list[str] = [
        _page_nav(depth=0, current="Overview"),
        DOC_HEADER,
        _render_overview(
            results, byte_map, names=names, sheet_compare=sheet_compare
        ),
        "\n",
        FRAME_SECTION.format(
            message_length=message_length,
            mfr=mfr,
            header=header,
        ),
    ]

    for result in results:
        parts.append(_summary_row(result) + "\n")

    parts.append(
        "\n## Parameter pages\n\n"
        "Index of all parameters with manual descriptions: "
        "[parameters/README.md](parameters/README.md).\n"
    )
    parts.append(f"\n_Last exported: {today}_\n")
    parts.append(
        render_open_items_section(PROG_OPEN_ITEMS, footer=OPEN_QUESTIONS_FOOTER)
    )
    return "".join(parts)


def _render_overview(
    results: list[dict[str, Any]],
    byte_map: dict[str, Any] | None,
    names: dict[str, Any] | None = None,
    sheet_compare: dict[str, Any] | None = None,
) -> str:
    """High-level status driven by parameter fits + full byte map coverage."""
    from ..names import format_bank_map_by_index

    total_params = len(results)
    by_conf = {"high": [], "medium": [], "low": []}
    for result in sorted(results, key=lambda r: r["parameter"]):
        conf = (result.get("hypothesis") or {}).get("confidence") or "low"
        by_conf.setdefault(conf, [])
        best = result.get("best_encoding") or {}
        offsets = best.get("offsets") or []
        enc = best.get("encoding") or "?"
        slug = parameter_slug(result["parameter"])
        by_conf[conf].append(
            f"[`{result['parameter']}`](parameters/{slug}.md) @ "
            f"{_fmt_offsets(offsets)} (`{enc}`)"
        )

    lines: list[str] = [
        "## Overview",
        "",
        f"_Snapshot from export on {date.today().isoformat()}._",
        "",
        "Each `sysex/prog/parameters/<parameter>/` folder is an **independent** dump stream "
        "(only that parameter intentionally varied). Folders are never "
        "byte-compared against each other - other parameters may differ "
        "between series. Meta folder `sysex/prog/presets/` captures whole presets "
        "as `<bank>.<preset>.syx` for program-identity fields.",
        "",
    ]

    if names:
        hyp = names.get("hypothesis") or {}
        bank_map = ((names.get("fields") or {}).get("bank_index") or {}).get("map") or {}
        bank_bits = format_bank_map_by_index(bank_map)
        lines.extend(
            [
                "### Program identity (`sysex/prog/presets/`)",
                "",
                f"Confidence: **{hyp.get('confidence', '?')}**. "
                f"Details: [program-identity.md](program-identity.md) · "
                f"[presets/](presets/)"
                + (
                    " · [preset-sheet.md](preset-sheet.md)"
                    if sheet_compare
                    else ""
                )
                + ".",
                "",
                f"- Name field 8-87 matches filename preset ({names.get('dump_count', 0)} dumps).",
                f"- Bank index 88-89 (`nibble_hilo`): {bank_bits or '(see page)'}.",
                "- Program slot 90-91 (`nibble_hilo`) within the current bank.",
                "- Offset 137 mirrors bank index low nibble (89).",
                "",
            ]
        )
        if sheet_compare:
            lines.extend(
                [
                    f"- Sheet errata: **{sheet_compare.get('hard_count', 0)}** hard / "
                    f"**{sheet_compare.get('soft_only_count', 0)}** soft-only vs "
                    f"[Bricasti preset sheet](preset-sheet.md).",
                    "",
                ]
            )

    if byte_map:
        counts = byte_map["counts"]
        total = counts["total"] or 1
        known_pct = 100.0 * counts["known_or_frame"] / total
        unknown_pct = 100.0 * counts["unknown"] / total
        secondary_pct = 100.0 * counts["secondary"] / total
        lines.extend(
            [
                "### Byte coverage",
                "",
                f"Program-dump layout is **{total} bytes** "
                "(roles merged from independent per-parameter conclusions). "
                "Full table: [byte-map.md](byte-map.md) · "
                "short overview: [byte-map-overview.md](byte-map-overview.md). "
                "Codegen: [m7_program_dump.ksy](m7_program_dump.ksy) / "
                "[m7_program_dump.spec.json](m7_program_dump.spec.json).",
                "",
                "| Status | Bytes | Share |",
                "|--------|------:|------:|",
                f"| Known / frame / checksum | {counts['known_or_frame']} | {known_pct:.0f}% |",
                f"| Secondary (moved within a series) | {counts['secondary']} | {secondary_pct:.0f}% |",
                f"| Unknown | {counts['unknown']} | {unknown_pct:.0f}% |",
                "",
            ]
        )

        unknown_regions = [
            r for r in byte_map.get("regions") or [] if r.get("status") == "unknown"
        ]
        if unknown_regions:
            lines.append("Largest unknown payload regions:")
            lines.append("")
            for region in sorted(
                unknown_regions, key=lambda r: r["length"], reverse=True
            )[:5]:
                example = region.get("example_hex")
                suffix = f" - e.g. `{example}`" if example else ""
                lines.append(
                    f"- offsets **{region['offsets']}** "
                    f"({region['length']} byte{'s' if region['length'] != 1 else ''})"
                    f"{suffix}"
                )
            lines.append("")

        known_params = [
            r
            for r in byte_map.get("regions") or []
            if r.get("status") == "known"
        ]
        if known_params:
            from ..byte_map import _linkify_meaning, _series_parameter_names

            series_params = _series_parameter_names(byte_map)
            lines.append("Identified parameter fields (each from its own folder):")
            lines.append("")
            for region in known_params:
                role = _linkify_meaning(region["role"], series_params)
                lines.append(f"- **{region['offsets']}** - {role}")
            lines.append("")
    else:
        lines.extend(
            [
                "### Byte coverage",
                "",
                "_Byte map unavailable._",
                "",
            ]
        )

    lines.extend(
        [
            "### Parameter reverse-engineering",
            "",
            f"**{total_params}** parameter series analyzed under `sysex/prog/parameters/`.",
            "",
        ]
    )
    for conf in ("high", "medium", "low"):
        items = by_conf.get(conf) or []
        if not items:
            continue
        lines.append(f"- **{conf} confidence ({len(items)}):** {', '.join(items)}")
    lines.append("")

    overview = catalog_overview([r["parameter"] for r in results])
    lines.extend(
        [
            "### Official program parameters",
            "",
            overview["disclaimer"],
            "",
            f"Catalog hints: **{overview['program_parameter_count']}** published controls; "
            f"**{len(overview['captured'])}** matched to capture folders; "
            f"**{len(overview['missing'])}** still uncaptured.",
            "",
        ]
    )
    if overview["missing"]:
        lines.append("Still to capture (suggested folder names; ranges are hints):")
        lines.append("")
        for miss in overview["missing"]:
            lines.append(
                f"- `{miss['folder_hint']}` - {miss['name']} "
                f"({miss['range']})"
            )
        lines.append("")
        sheet_only = [
            d
            for d in ((names or {}).get("parameter_decoders") or [])
            if d.get("source") == "preset_sheet"
        ]
        if sheet_only:
            lines.append(
                "Already located via presets × published sheet "
                "(medium confidence until a dedicated series exists):"
            )
            lines.append("")
            for d in sheet_only:
                offs = d.get("offsets") or []
                off_txt = (
                    f"{offs[0]}-{offs[-1]}" if len(offs) > 1 else str(offs[0] if offs else "?")
                )
                lines.append(
                    f"- `{d['parameter']}` @ {off_txt} "
                    f"(`{d.get('encoding', '?')}`, {d.get('kind', '?')})"
                )
            lines.append("")
    lines.append("See also [parameter-catalog.md](../../docs/parameter-catalog.md).")
    lines.append("")

    lines.extend(
        [
            "### How to read these pages",
            "",
            "1. **This overview** - frame layout, parameter map, coverage.",
            "2. **[program-identity.md](program-identity.md)** / "
            "**[presets/](presets/)** - name / bank / slot; bank pages + "
            "[presets.json](presets/presets.json).",
            "3. **[preset-inventory.md](preset-inventory.md)** - capture count vs "
            "V2 addendum factory lists.",
            "4. **[preset-sheet.md](preset-sheet.md)** - errata vs Bricasti's "
            "published preset sheet PDF.",
            "5. **[parameters/README.md](parameters/README.md)** - all parameters with "
            "manual descriptions; each series also has dump tables and how-to-set notes "
            "under **[parameters/](parameters/)**.",
            "6. **[byte-map-overview.md](byte-map-overview.md)** / "
            "**[byte-map.md](byte-map.md)** - consolidated layout, then full detail.",
            "7. **[m7_program_dump.ksy](m7_program_dump.ksy)** / "
            "**[m7_program_dump.spec.json](m7_program_dump.spec.json)** - "
            "Kaitai Struct layout + machine schema for codegen.",
            "8. **[cross.md](cross.md)** - stable bytes, conflicts, coverage "
            "gaps across series.",
            "",
        ]
    )
    return "\n".join(lines)


def _export_preset_docs(
    output_dir: Path,
    names: dict[str, Any],
    *,
    today: str,
    sheet_compare: dict[str, Any] | None = None,
) -> int:
    """Write presets/ bank indexes, one page per preset, and presets.json."""
    from ..decode_preset import PARAMETER_ORDER

    sorted_dumps = sorted(
        names.get("dumps") or [],
        key=lambda row: (row.get("bank_index", 0), row.get("program_slot", 0)),
    )
    param_names = [
        p
        for p in PARAMETER_ORDER
        if any(p in (d.get("parameters") or {}) for d in sorted_dumps)
    ]
    decoders = names.get("parameter_decoders") or []

    by_bank: dict[str, list[dict[str, Any]]] = {}
    for d in sorted_dumps:
        by_bank.setdefault(d["bank"], []).append(d)

    presets_root = output_dir / "presets"
    presets_root.mkdir(parents=True, exist_ok=True)
    payload = {
        "generated": today,
        "source": "sysex/prog/presets/",
        "dump_count": len(sorted_dumps),
        "presets": sorted_dumps,
    }
    (presets_root / "presets.json").write_text(
        json.dumps(payload, indent=2) + "\n", encoding="utf-8"
    )

    for bank_name, dumps in by_bank.items():
        bank_slug, _ = preset_slugs(bank_name, dumps[0]["preset"])
        bank_folder = presets_root / bank_slug
        bank_folder.mkdir(parents=True, exist_ok=True)
        sorted_bank = sorted(dumps, key=lambda d: d.get("program_slot", 0))
        for dump in sorted_bank:
            _, preset_slug = preset_slugs(dump["bank"], dump["preset"])
            (bank_folder / f"{preset_slug}.md").write_text(
                _render_preset_page(
                    dump,
                    all_dumps=sorted_dumps,
                    bank_siblings=sorted_bank,
                    param_names=param_names,
                    decoders=decoders,
                    today=today,
                    sheet_compare=sheet_compare,
                ),
                encoding="utf-8",
            )
        (bank_folder / "README.md").write_text(
            _render_bank_page(
                bank_name,
                dumps,
                param_names=param_names,
                decoders=decoders,
                today=today,
                sheet_compare=sheet_compare,
            ),
            encoding="utf-8",
        )

    (presets_root / "README.md").write_text(
        _render_presets_index(sorted_dumps, today=today),
        encoding="utf-8",
    )
    return len(sorted_dumps)


def _preset_sheet_callout(
    sheet_status: dict[str, Any], *, link_prefix: str = "../../"
) -> list[str]:
    """Banner above the parameters table describing sheet coverage / errata."""
    kind = sheet_status.get("kind")
    if kind == "compared":
        comparison = sheet_status["comparison"]
        n_hard = len(comparison.get("hard") or [])
        n_soft = len(comparison.get("soft") or [])
        if n_hard or n_soft:
            parts: list[str] = []
            if n_hard:
                parts.append(f"{n_hard} hard")
            if n_soft:
                parts.append(f"{n_soft} soft")
            summary = " / ".join(parts)
            lines = [
                f"> **Sheet discrepancies ({summary}):** dump values disagree "
                "with the [published preset sheet]("
                f"{link_prefix}preset-sheet.md). "
                "Highlighted in the Status column below. "
                "**SysEx dump is authoritative.**",
                "",
            ]
            if n_hard:
                lines.append(
                    "> Hard = beyond tolerance (likely sheet print error or "
                    "unit/firmware variance)."
                )
            if n_soft:
                lines.append(
                    "> Soft = within table/rounding band (sparse-table "
                    "interpolation or print rounding)."
                )
            if n_hard or n_soft:
                lines.append("")
            return lines
        return [
            "> **Sheet comparison:** all printable sheet columns match the "
            "decoded dump (within tolerance).",
            "",
        ]
    if kind == "missing":
        return [
            "> **Not on the published sheet:** this factory dump has no row "
            "on the classic [preset sheet PDF]("
            f"{link_prefix}preset-sheet.md) "
            "(likely a later bank addition). Dump values only below.",
            "",
        ]
    if kind == "skipped":
        reason = sheet_status.get("reason") or "not listed on the classic sheet"
        return [
            f"> **Sheet comparison skipped:** {reason}. "
            f"See [preset-sheet.md]({link_prefix}preset-sheet.md). Dump values only "
            "below.",
            "",
        ]
    return []


def _format_sheet_delta(detail: dict[str, Any]) -> str:
    if "delta" not in detail:
        return ""
    try:
        delta = float(detail["delta"])
    except (TypeError, ValueError):
        return ""
    if abs(delta) < 1e-9:
        return ""
    if abs(delta - round(delta)) < 1e-9:
        return f" (Δ {int(round(delta)):+d})"
    text = f"{delta:+.4g}".rstrip("0").rstrip(".")
    return f" (Δ {text})"


