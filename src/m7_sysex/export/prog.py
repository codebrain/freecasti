def _parameter_description_section(
    official: dict[str, Any] | None,
    *,
    parameter: str | None = None,
) -> list[str]:
    """Official manual prose for a parameter page (before dump tables)."""
    meta = dict(official or {})
    if not meta.get("description") and parameter:
        from ..catalog import lookup_parameter

        catalog = lookup_parameter(parameter)
        if catalog and catalog.get("description"):
            meta = catalog
    if not meta.get("description"):
        return []
    lines = [
        "## Description",
        "",
        str(meta["description"]),
        "",
    ]
    sources = list(meta.get("description_sources") or [])
    if not sources:
        cite = meta.get("description_source")
        url = meta.get("description_url")
        if cite and url:
            sources = [(cite, url)]
        elif cite:
            sources = [(cite, None)]
    if sources:
        parts = []
        for cite, url in sources:
            if url:
                parts.append(f"[{cite}]({url})")
            else:
                parts.append(str(cite))
        lines.append(f"_Source: {'; '.join(parts)}._")
        lines.append("")
    return lines


